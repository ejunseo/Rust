// 1. x와 y 좌표를 가진 'Point'라는 구조체를 정의합니다.
struct Point { x: i32, y: i32 }

fn main() {
    // 2. x는 10, y는 0인 점을 하나 만듭니다.
    let p = Point { x: 10, y: 0 };

    // 3. p의 내부를 들여다보며 매칭을 시작합니다.
    match p {
        // 4. [구조 분해 1] y가 정확히 0인 경우를 찾습니다.
        // 이때 x의 값은 'x'라는 이름의 변수에 바로 담깁니다. (알맹이 빼오기)
        Point { x, y: 0 } => println!("X축 위의 점, x 좌표는: {}", x),
       
        // 5. [구조 분해 2] 그 외 나머지 모든 경우입니다.
        // p 안에 있던 x, y 값을 각각 새로운 변수 x, y에 담아서 출력합니다.
        Point { x, y } => println!("일반 좌표: ({}, {})", x, y),
    }
}
