fn main() {
    // 소유자 s 등장 (수정 가능해야 하므로 mut)
    let mut s = String::from("hello");

    // 1. 불변 빌림 (여러 개 가능)
    let r1 = &s; 
    let r2 = &s;

    // 2. 가변 빌림 불가능 (반납 안 끝나서)
    // let r3 = &mut s; 

    println!("불변 빌림 사용 중: {} and {}", r1, r2); 
    // --- r1, r2의 사용이 여기서 끝남 (빌림 반납) ---

    // 3. 가변 빌림 (이제 가능!)
    let r3 = &mut s; 

    // 4. 값 수정
    r3.push_str(", world"); // 문자열을 붙일 때
    r3.push('!');           // 문자 하나(' ')를 붙일 때

    // 5. 수정된 결과 출력
    println!("가변 빌림을 통해 수정된 값: {}", r3);
    
    // 6. 최종 소유자가 확인한 값
    println!("원본 소유자 s의 최종 상태: {}", s);
}
